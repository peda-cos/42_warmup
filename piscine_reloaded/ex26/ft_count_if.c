int	ft_count_if(char **tab, int (*f)(char *))
{
	int	count;

	count = 0;
	if (tab == 0 || f == 0)
		return (0);
	while (*tab)
	{
		if (f(*tab) == 1)
			count++;
		tab++;
	}
	return (count);
}
